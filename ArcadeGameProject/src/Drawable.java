import java.awt.Color;
import java.awt.Shape;
/*
 * this interface allows us to group up things that need to be
 * drawn and also gives them methods to get their color and 
 * shape so they have a defined drawing pattern;
 */
public interface Drawable {
	/*
	 * returns color of object
	 */
	Color getColor();

	/*
	 * returns shape of object
	 */
	Shape getShape();
}
